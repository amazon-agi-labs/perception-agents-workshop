def parse_steps(content):
    """Parse Gherkin .feature file into steps"""
    steps = []
    last_type = 'then'
    for line in content.splitlines():
        stripped = line.strip()
        if stripped.startswith('Given '):
            steps.append(('given', stripped[6:]))
            last_type = 'given'
        elif stripped.startswith('When '):
            steps.append(('when', stripped[5:]))
            last_type = 'when'
        elif stripped.startswith('Then '):
            steps.append(('then', stripped[5:]))
            last_type = 'then'
        elif stripped.startswith('And '):
            steps.append((last_type, stripped[4:]))
    return steps


def run_verification(app_url, app_dir, rules, flows):
    """Run visual + flow verification using NovaAct SDK"""
    print(f"[start] Verifying {app_url}...", file=sys.stderr)

    visual_results = []
    flow_results = []

    logs_dir = str(Path(app_dir) / 'logs' / 'nova_act')
    os.makedirs(logs_dir, exist_ok=True)

    with NovaAct(starting_page=app_url, headless=True, nova_act_api_key=os.environ.get('NOVA_ACT_API_KEY', ''), ignore_https_errors=True, logs_directory=logs_dir) as nova:
        # ── Visual checks: evaluate JS directly via Playwright (no LLM needed) ──
        if rules:
            print(f"[visual] Checking {len(rules)} CSS rules...", file=sys.stderr)
            script = build_check_script(rules)
            try:
                page = nova.page
                raw = page.evaluate(script)
                visual_results = json.loads(raw) if isinstance(raw, str) else raw
                print(f"[visual] Got {len(visual_results)} results", file=sys.stderr)
            except Exception as e:
                print(f"[visual] Evaluate failed: {e}", file=sys.stderr)
                visual_results = [{'name': r['name'], 'pass': False, 'actual': 'EVAL_ERROR', 'expected': r['constraint']} for r in rules]

            # Capture screenshot (separate try so it doesn't affect results)
            try:
                screenshots_dir = Path(app_dir) / '.ui-verification' / 'reports' / '_pending_screenshots'
                screenshots_dir.mkdir(parents=True, exist_ok=True)
                nova.page.screenshot(path=str(screenshots_dir / 'full-page.png'), full_page=True)
                print(f"[visual] Screenshot captured", file=sys.stderr)
            except Exception as e:
                print(f"[visual] Screenshot failed: {e}", file=sys.stderr)

        # ── Flow checks ──
        for flow in flows:
            print(f"[flow] Running {flow['name']}...", file=sys.stderr)
            steps = parse_steps(flow['content'])
            step_results = []
            flow_pass = True

            for stype, text in steps:
                if stype in ('given', 'when'):
                    try:
                        if 'scroll to the bottom' in text or 'scroll to' in text.lower():
                            nova.act("Scroll to the very bottom of the page", max_steps=10)
                            step_results.append({'step': text, 'pass': True, 'type': stype})
                        elif 'page finishes loading' in text or 'I am on' in text:
                            step_results.append({'step': text, 'pass': True, 'type': stype})
                        else:
                            nova.act(text, max_steps=10)
                            step_results.append({'step': text, 'pass': True, 'type': stype})
                    except Exception as e:
                        step_results.append({'step': text, 'pass': False, 'type': stype})
                        flow_pass = False
                else:
                    # Then/And — verify assertion
                    try:
                        nova.act(f"Verify: {text}. Return PASS if true, FAIL if not.", max_steps=10)
                        step_results.append({'step': text, 'pass': True, 'type': stype})
                    except Exception as e:
                        step_results.append({'step': text, 'pass': False, 'type': stype})
                        flow_pass = False

            flow_results.append({
                'name': flow['name'],
                'pass': flow_pass,
                'steps': step_results,
                'type': flow.get('type', 'state_completeness'),
                'feature_title': flow.get('feature_title', flow['name']),
                'scenario_title': flow.get('scenario_title', 'Verification scenario'),
            })

    return visual_results, flow_results


