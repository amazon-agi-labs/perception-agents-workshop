def _write_flow_report(report_dir, flow, app_url):
    """Write an individual flow report file matching the ui-verification skill format"""
    flow_reports_dir = report_dir / 'flow-reports'
    flow_reports_dir.mkdir(parents=True, exist_ok=True)

    status = 'PASS' if flow['pass'] else 'FAIL'
    flow_type = flow.get('type', 'state_completeness')
    feature_title = flow.get('feature_title', flow['name'])
    scenario_title = flow.get('scenario_title', 'Verification scenario')
    run_date = datetime.now(timezone.utc).strftime('%Y-%m-%dT%H:%M:%SZ')

    fr = f"""# Flow Report: {flow['name']}

**Flow:** {flow['name']} — {feature_title}  
**Type:** {flow_type}  
**URL:** {app_url}  
**Status:** {status}  
**Run date:** {run_date}

---

## Scenario: {scenario_title}

| Step | Type | Action/Assertion | Result |
|------|------|------------------|--------|
"""
    for s in flow['steps']:
        stype = s.get('type', 'then').capitalize()
        result = 'PASS' if s['pass'] else 'FAIL'
        if stype in ('Given', 'When'):
            result = 'OK' if s['pass'] else 'FAIL'
        fr += f"| {s['step'][:60]} | {stype} | {s['step']} | {result} |\n"

    passed_count = sum(1 for s in flow['steps'] if s['pass'])
    total_count = len(flow['steps'])

    if flow['pass']:
        fr += f"\n**All {total_count} assertions passed.**\n"
    else:
        fr += f"\n**{passed_count}/{total_count} assertions passed.**\n"

    # Check for failures and add details
    failures = [s for s in flow['steps'] if not s['pass']]
    if failures:
        fr += "\n---\n\n## Failures\n\n"
        for s in failures:
            fr += f"- ❌ **{s['step']}**\n"
        fr += "\n"

    flow_report_path = flow_reports_dir / f"{flow['name']}.report.md"
    flow_report_path.write_text(fr)
    print(f"[flow] Wrote {flow_report_path}", file=sys.stderr)


def _get_analysis(name, expected, actual):
    """Generate a brief analysis for a failure"""
    if 'line height' in name.lower() and 'px' in actual:
        return "Computed value resolves unitless to px"
    if 'margin' in name.lower() and expected == '=auto' and 'px' in actual:
        return "Computed resolves auto to px (element is centered)"
    if 'exists' in expected:
        return "Constraint engine limitation — element IS present"
    if 'color' in name.lower():
        return "Color mismatch — may be intentional change"
    return "Value differs from spec"


